package pers.mihao.careerism.design_patterns.creater_type.builder;

public class Pc {

    private Object cpu;

    private Object scream;

    private Object key;

    public Object getCpu() {
        return cpu;
    }

    public void setCpu(Object cpu) {
        this.cpu = cpu;
    }

    public Object getScream() {
        return scream;
    }

    public void setScream(Object scream) {
        this.scream = scream;
    }

    public Object getKey() {
        return key;
    }

    public void setKey(Object key) {
        this.key = key;
    }
}
